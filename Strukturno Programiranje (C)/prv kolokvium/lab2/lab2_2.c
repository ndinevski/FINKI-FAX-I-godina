/*
Напишете код со којшто ќе вчитате три вредности x,y,z, 
и потоа еден по еден ќе ги извршите следниве изрази:

y = ++x && (z+5 || --y)
z = z-- || (x>y) && (x>z)
Испечатете ги вредностите на x,y,z по секое извршување.
*/
#include <stdio.h>

int main(){
    int x,y,z;
    scanf("%d%d%d", &x, &y, &z);
    y = ++x && (z+5 || --y);
    printf("%d %d %d\n", x, y, z);
    z = z-- || (x>y) && (x>z);
    printf("%d %d %d", x, y, z);
    return 0;
}
    